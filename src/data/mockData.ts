export interface Seller {
  id: string;
  username: string;
  displayName: string;
  avatar: string;
  location: string;
  bio: string;
  followers: number;
  following: number;
  posts: number;
}

export interface SalePost {
  id: string;
  type: "sale";
  seller: Seller;
  images: string[];
  title: string;
  price: number;
  condition: "New" | "Like New" | "Good" | "Fair";
  category: "Furniture" | "Clothing" | "Electronics" | "Books" | "Decor" | "Other";
  description: string;
  likes: number;
  comments: number;
  liked: boolean;
  createdAt: string;
  hashtags: string[];
}

export interface ISOPost {
  id: string;
  type: "iso";
  seller: Seller;
  title: string;
  description: string;
  budget?: number;
  category: "Furniture" | "Clothing" | "Electronics" | "Books" | "Decor" | "Other";
  likes: number;
  comments: number;
  liked: boolean;
  createdAt: string;
  hashtags: string[];
}

export type Post = SalePost | ISOPost;

export interface Message {
  id: string;
  senderId: string;
  text: string;
  timestamp: string;
}

export interface Conversation {
  id: string;
  type: "buying" | "selling";
  listing: { id: string; title: string; image: string; price: number };
  otherUser: Seller;
  messages: Message[];
  unread: boolean;
}

